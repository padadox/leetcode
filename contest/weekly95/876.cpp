  struct ListNode {
      int val;
      ListNode *next;
      ListNode(int x) : val(x), next(NULL) {}
  };
class Solution {
public:
    ListNode* middleNode(ListNode* head) {
        ListNode* m = head;
        ListNode* p = head;

        while (p != NULL && p->next != NULL) {
            m = m->next;
            p = p->next;
            if(p) {
                p = p->next;
            }
        }

        return m;
    }
};
