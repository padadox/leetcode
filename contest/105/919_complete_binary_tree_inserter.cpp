/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
struct TreeNode {
    int val;
    TreeNode *left;
    TreeNode *right;
    TreeNode(int x) : val(x), left(NULL), right(NULL) {}
};

/**
 * 层次遍历(广度优先搜索），找到第一个不完全的点。
 *
 */


class CBTInserter {
    TreeNode *root = nullptr;
    queue<TreeNode*> q;
public:
    CBTInserter(TreeNode* root) {
        this->root = root;
        q.push(root);
        while(!q.empty()) {
            auto x = q.front();

            if(nullptr == x->left) {
                break;
            }
            q.push(x->left);

            if(nullptr == x->right) {
                break;
            }
            q.push(x->right);
            
            q.pop();
        }
    }
    
    int insert(int v) {
        TreeNode *t = new TreeNode(v);
        auto x = q.front();
        if(nullptr == x->left) {
            x->left = t;
            q.push(t);
            return x->val;
        }

        x->right = t;
        q.push(t);
        q.pop();
        return x->val;
    }
    
    TreeNode* get_root() {
        return root;
    }
};

