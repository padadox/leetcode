class Solution {
public:
    string orderlyQueue(string S, int K) {
        if(K > 1) {
            sort(S.begin(),  S.end());
            return S;
        }

        
        string m = S;

        for(size_t i = 1; i < S.size(); ++i) {
            string x = S.substr(i) + S.substr(0, i);
            if(x < m) {
                m = x;
            }
        }

        return m;
    }
};
